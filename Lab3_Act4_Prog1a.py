# Ohm's Law: resistance (ohms), current (amperes) --> voltage (volts)
print("Given a resistance and current, this program calculates the voltage.")
resistance = float(input("What is the resistance? (ohms): "))
current = float(input("What is the current? (amperes): "))
print("The voltage across the conductor is", resistance * current, "volts.")