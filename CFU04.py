print("This program returns the entree of the meal of the day")
day = input("Please enter a weekday: ")
if(day=="Monday"):
    print("The entree for Monday is a burger on a bun")
elif(day=="Tuesday"):
    print("The entree for Tuesday is a burrito")
elif(day=="Wednesday"):
    print("The entree for Wednesday is spaghetti")
elif(day=="Thursday"):
    print("The entree for Thursday is a Salisbury steak")
elif(day=="Friday"):
    print("The entree for Friday is a fish sandwich")
else:
    print("Please enter a valid input")
    exit()
