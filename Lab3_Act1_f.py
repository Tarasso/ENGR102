print("This program converts Degrees Fahrenheit to Degrees Celsius")
degreesFahrenheit = float(input("Degrees: "))
degreesCelcius = (degreesFahrenheit - 32) * 5 / 9
print(f"{degreesFahrenheit} F = {degreesCelcius} C")